	<h3>
			<u>Missione del FESR (Fondo Europeo di Sviluppo Regionale)  :</u>
	</h3>
		<p>Contribuire finanziariamente al potenziamento della coesione economica, sociale e territoriale riducendo le disparit&agrave;  regionali e sostenendo lo sviluppo e l&rsquo;adeguamento strutturale delle economie regionali, compresa la riconversione delle regioni industriali in declino.
		</p>
		
		<h3>
			<u>Le azioni del Fondo:</u>
		</h3>
		<p>L&rsquo;azione del Fondo si concentrer&agrave; su alcune priorit&agrave; tematiche, in particolare la competitivit&agrave;, l&rsquo;innovazione, la creazione di posti di lavoro sostenibili e una crescita rispettosa dell&rsquo;ambiente.
		</p>
		<p>
			Il Fondo europeo di sviluppo regionale finanzier&agrave; anche interventi nel quadro dei tre nuovi obiettivi della politica di coesione riformata: &laquo; Convergenza&raquo; &laquo; Competitivit&agrave; regionale e occupazione&raquo; e &laquo; Cooperazione territoriale europea &raquo;.
		</p>
			<h3><u>Nuovo periodo di programmazione 2014-2020:</u> </h3>
		<p>L&rsquo;orientamento generale del nuovo periodo di programmazione prevede un&rsquo;economia a bassa emissione di CO2 nell&rsquo;insieme dei settori economici, conformemente agli obiettivi della nuova Strategia &laquo;Europa 2020&raquo; .
Altra grande linea del nuovo periodo di programmazione: lo sviluppo urbano sostenibile.
		</p>

