Le covoiturage est de ce fait fortement encouragé par les autorités, notamment lors des pics de pollution. Il semble
d'autant plus pratiqué que le prix du carburant augmente ou en cas de grève des transports. La loi interdit qu'il
devienne une pratique professionnelle (qui serait concurrentielle aux activités marchandes de transports publics ou
privés) : le conducteur ne doit donc pas faire de bénéfices. Il dépend de la motivation des particuliers et repose en
grande partie sur un principe collaboratif d'auto-organisation mais peut être encouragé, co-organisé ou aidé par des
collectivités ou entreprises qui y trouvent divers bénéfices.
