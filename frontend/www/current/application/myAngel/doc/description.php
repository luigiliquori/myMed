Le service myAngel sera spécifiquement conçu pour venir en aide aux personnes (par
exemple personnes atteintes de maladies graves, personnes âgées, et jeunes enfants, mais
aussi lors sorties en montagne ou en mer dans l’espace Alcotra, etc.) qui ont besoin d'être
suivies 24h/24. myAngel est un service sensible à la géolocalisation qui permet à l'utilisateur
d'accepter que certaines personnes de sa liste de contacts puissent suivre ses mouvements.
Ces personnes peuvent suivre l'utilisateur (plus précisément son mobile) à travers la localisation
automatique utilisée dans les réseaux cellulaires, l'adresse IP ou encore le système GPS.
